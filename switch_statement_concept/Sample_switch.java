public class Sample_switch {
    public static void main(String[] args) {
    for(int i=0;i<=10;i++){
        switch(i) {
            case 0:
                System.out.println("i is 0");
                break;
            case 1:
                System.out.println("i is 1");
                break;    
            case 2:
                System.out.println("i is 2");
                break;
            default:
                System.out.println("i is greater than 2");
                break;
        }
    }
    }
    
}
