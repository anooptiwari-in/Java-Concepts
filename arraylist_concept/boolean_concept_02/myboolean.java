import java.util.Scanner;
public class myboolean {
    public static void main(String[] args) {
       int i,j;
       Scanner in = new Scanner(System.in);
       i = in.nextInt();
       j = in.nextInt();
       System.out.println(i==j);
       System.out.println(i<j);
       System.out.println(i>j);
       System.out.println(i!=j);
    }
    
}
