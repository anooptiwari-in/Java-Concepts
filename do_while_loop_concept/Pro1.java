package do_while_loop_concept;

public class Pro1 {
    public static void main(String[] args) {
        int i=5;
        while(i>0){
            System.out.print(i + " ");
            i--;
        }
        System.out.print("\n");
        
        int j = 5;
        do{
            System.out.print(j + " ");
            j--;
        } while(j>0);
        
        System.out.print("\n");
        for(int a=1;a<5;a++){
            System.out.print(a + " ");
        }
    }
    
    
}
